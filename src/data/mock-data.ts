// ─── Date Helpers ─────────────────────────────────────────────────────────────
// Today: 2026-03-04
const daysAgo = (n: number): string => {
  const d = new Date("2026-03-04T00:00:00Z");
  d.setDate(d.getDate() - n);
  return d.toISOString().replace(".000Z", "Z");
};
const daysAgoDate = (n: number): string => daysAgo(n).split("T")[0];

