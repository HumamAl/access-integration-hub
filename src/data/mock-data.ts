// ─── Lookup Helpers ───────────────────────────────────────────────────────────
export const getCardholderById = (id: string): Cardholder | undefined =>
  cardholders.find((c) => c.id === id);

export const getCredentialsByCardholder = (cardholderId: string): Credential[] =>
  credentials.filter((c) => c.cardholderId === cardholderId);

export const getAccessLevelById = (id: string): AccessLevel | undefined =>
  accessLevels.find((a) => a.id === id);

export const getControllerById = (id: string): Controller | undefined =>
  controllers.find((c) => c.id === id);

export const getEventsByCardholder = (cardholderId: string): AccessEvent[] =>
  accessEvents.filter((e) => e.cardholderId === cardholderId);

export const getEventsByController = (controllerId: string): AccessEvent[] =>
  accessEvents.filter((e) => e.controllerId === controllerId);

export const getSyncsByProvider = (provider: string): SyncOperation[] =>
  syncOperations.filter((s) => s.provider === provider || s.provider === "Both");

export const getUnacknowledgedEvents = (): AccessEvent[] =>
  accessEvents.filter((e) => !e.acknowledged);

export const getFailedSyncOperations = (): SyncOperation[] =>
  syncOperations.filter((s) => s.status === "Failed" || s.status === "Partial");
